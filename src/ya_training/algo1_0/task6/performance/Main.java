package ya_training.algo1_0.task6.performance;

/*В лицее на уроках информатики ответы учеников оцениваются целым числом баллов от 2 до 5 . Итоговая оценка по информатике
выставляется как среднее арифметическое оценок на всех уроках, округленное до ближайшего целого числа. Если среднее значение
находится ровно посередине между двумя целыми числами, то оценка округляется вверх.
Все ученики лицея стремятся получить итоговую оценку по информатике не ниже 4 баллов. К сожалению, один из учеников получил на
уроках a двоек, b троек и c четверок. Теперь он планирует получить несколько пятерок, причем хочет, чтобы итоговая оценка была
не меньше 4 баллов. Ему надо понять, какое минимальное количество пятерок ему необходимо получить, чтобы добиться своей цели.

Требуется написать программу, которая по заданным целым неотрицательные числам a , b и c определяет минимальное количество пятерок,
которое необходимо получить ученику, чтобы его итоговая оценка по информатике была не меньше 4 баллов.

Формат ввода
Входные данные содержат три строки. Первая строка содержит целое неотрицательное число a , вторая строка содержит целое
неотрицательное число b , третья строка содержит целое неотрицательное число c (0≤ a, b, c ≤10^15, a + b + c ≥1).

Формат вывода
Выходные данные должны содержать одно число: минимальное число пятерок, которое необходимо получить ученику, чтобы итоговая
оценка была не меньше 4 баллов.

Пример
Ввод	Вывод
2
0
0

2*/
import java.io.*;

public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));

        long a = Long.parseLong(reader.readLine()); // двойки
        long b = Long.parseLong(reader.readLine()); // тройки
        long c = Long.parseLong(reader.readLine()); // четверки

        long n = a + b + c;

        long sum = 2*a + 3*b + 4*c;

        long l= 0, r = n;

        while (r > l) {
            long m = (r + l) / 2;
            if ((sum + m*5)*10  >= 35*(n + m)) // средний балл должен быть больше 3.5
                r = m;
            else
                l = m + 1;
        }
        System.out.println(r);
        reader.close();
    }
}
